	function obtener_datos(id){
			var fecha = $("#fecha"+id).val();
			var hora = $("#hora"+id).val();
			var lugar = $("#lugar"+id).val();
			var numero_factura = $("#numero_factura"+id).val();
	
			$("#mod_nombre").val(fecha);
			$("#mod_telefono").val(hora);
			$("#mod_email").val(lugar);
			$("#mod_direccion").val(numero_factura);
			$("#mod_id").val(id);
		
		}
	
		
		

